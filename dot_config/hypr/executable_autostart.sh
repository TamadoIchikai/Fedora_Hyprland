print_centered() {
    local art="$1"
    local term_cols=$(tput cols 2>/dev/null || echo 80)

    local max_width=0
    local art_lines=0

    while IFS= read -r line; do
        (( ${#line} > max_width )) && max_width=${#line}
        (( ++art_lines ))
    done <<< "$art"

    local h_pad=$(( (term_cols - max_width) / 2 ))
    (( h_pad < 0 )) && h_pad=0

    local spaces=$(printf '%*s' "$h_pad" "")
    while IFS= read -r line; do
        echo "${spaces}${line}"
    done <<< "$art"
}

